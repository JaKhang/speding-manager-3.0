    public void previusMonth() {
        int month = selectMonth.get(Calendar.MONTH);
        int year = selectMonth.get(Calendar.YEAR);
        month--;
        if (month < Calendar.JANUARY) {
            year--;
            month = Calendar.DECEMBER;
        }
        selectMonth = new GregorianCalendar(year, month, 1);
        showMonth();
    }

    public void nextMonth() {
        int month = selectMonth.get(Calendar.MONTH);
        int year = selectMonth.get(Calendar.YEAR);
        month++;
        if (month > Calendar.DECEMBER) {
            year++;
            month = Calendar.JANUARY;
        }
        selectMonth = new GregorianCalendar(year, month, 1);
        showMonth();

    }


